@bot.message_handler(func=lambda message: text_button_3 == message.text)
def help_command(message):
    bot.send_message(message.chat.id, "[Пицца четыре сыра!](https://art-lunch.ru/recipe/pitstsa-4-syra_foto/?ysclid=lnu9gbad3x769531049)", reply_markup=menu_keyboard)  # Можно менять текст


